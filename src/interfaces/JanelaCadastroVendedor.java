    private void gravarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gravarButtonActionPerformed
        consultarButton.setEnabled(true);
        cancelaButton.setEnabled(false);
        String mensagem_erro = null;
        Usuario usuario;
        if (cod_usuarioTextField.getText().equals("")) {
            usuario = new Usuario(0);
        } else {
            usuario = new Usuario(Integer.valueOf(cod_usuarioTextField.getText()));
        }

        Vendedor vendedor = new Vendedor(Integer.valueOf(cod_vendedorTextField.getText()).intValue(), nome_vendedorTextField.getText(), Float.valueOf(comissaoTextField1.getText().replace(",", ".")), usuario);

        if (!dadosOK(vendedor)) {
            mensagem_erro = "O campo nome é obrigatório";
        } else {
            mensagem_erro = Vendedor.inserirVendedor(vendedor);
        }
        if (mensagem_erro != null) {
            JOptionPane.showMessageDialog(this, mensagem_erro, "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, nome_vendedorTextField.getText() + " Cadastrado com sucesso");
            limpa();
            setAtivo(false);
            novoButton.setEnabled(true);
        }


    }//GEN-LAST:event_gravarButtonActionPerformed
    public static boolean dadosOK(Vendedor vendedor) {
        if ((!vendedor.getNome().equals(""))) {
            return true;
        } else {
            return false;
        }
    }
