    private void cod_usuarioTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cod_usuarioTextFieldKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_F2) {
            JanelaConsulta<Usuario> user = new JanelaConsulta<Usuario>(new Usuario());
            user.setTitle("Consulta Usuario");
            user.setVisible(true);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (cod_usuarioTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Digite o código do Usuario");
                nome_usuarioTextField.setText("");
            } else {
                try {
                    Usuario user = Usuario.verificaUsuário(Integer.valueOf(cod_usuarioTextField.getText()));
                    nome_usuarioTextField.setText(user.getNome());
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, ex, "ERRO", JOptionPane.ERROR_MESSAGE);
                }
                nome_usuarioTextField.grabFocus();
            }
        }

    }//GEN-LAST:event_cod_usuarioTextFieldKeyPressed

