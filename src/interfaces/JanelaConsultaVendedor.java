    private void consulta_vendedorTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_consulta_vendedorTableMousePressed
        if (evt.getClickCount() == 2) {
            int linha = consulta_vendedorTable.getSelectedRow();
            int colunaCod = 0;
            String codigo_temp = consulta_vendedorTable.getValueAt(linha, colunaCod).toString();
            int codigo = Integer.valueOf(codigo_temp).intValue();
            Vendedor vendedor = Vendedor.buscarVendedor(codigo);
            if(getTitle().equalsIgnoreCase("Vendedor")){
               JanelaCadastroVendedor.mostraDados(vendedor);
               dispose();
            }
            /*
            }else if (getTitle().equalsIgnoreCase("Selecione o cliente para a venda")){
               JanelaVenda.mostraDadosCliente(cliente);
               dispose();
            }
            */
            
        }
    }//GEN-LAST:event_consulta_vendedorTableMousePressed


