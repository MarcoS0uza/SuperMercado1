    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        Usuario usuário = new Usuario(Integer.valueOf(cod_usuarioTextField.getText()).intValue(),
                nome_usuarioTextField.getText(), senhaPasswordField.getText(), cargoComboBox.getSelectedItem().toString());
        String mensagem_erro;
        if (!dadosOK(usuário)) {
            mensagem_erro = "Não pode conter registros em branco";
        } else if (!confereSenhaIguais()) {
            mensagem_erro = "Senhas não conferem";
        } else {
            mensagem_erro = controlador.alterarUsuário(usuário);
        }
        if (mensagem_erro != null) {
            JOptionPane.showMessageDialog(this, mensagem_erro, "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, nome_usuarioTextField.getText() + " alterado com sucesso");

        }
            setEdição(false);
            editarSalvar();
    }//GEN-LAST:event_salvarButtonActionPerformed

