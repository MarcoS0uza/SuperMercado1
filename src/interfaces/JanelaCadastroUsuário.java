    public static void limpa() {
        nome_usuarioTextField.setText("");
        cargoComboBox.setSelectedIndex(0);
        senhaPasswordField.setText("");
        confirma_senhaPasswordField.setText("");
    }

    public static void editarSalvar() {
        salvarButton.setVisible(false);
        editar_Button.setVisible(true);
    }

    public static void setEdição(boolean estado) {
        nome_usuarioTextField.setEditable(estado);
        cargoComboBox.setEditable(estado);
        senhaPasswordField.setEditable(estado);
        confirma_senhaPasswordField.setEditable(estado);
    }

    public static void setAtivo(boolean estado) {
        nome_usuarioTextField.setEnabled(estado);
        cargoComboBox.setEnabled(estado);
        senhaPasswordField.setEnabled(estado);
        confirma_senhaPasswordField.setEnabled(estado);
        eliminarButton.setEnabled(estado);
        gravarButton.setEnabled(estado);
        editar_Button.setEnabled(estado);
    }

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        int eliminar = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar este registro", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (eliminar == JOptionPane.YES_OPTION) {
            String mensagem_erro = controlador.removerUsuário(nome_usuarioTextField.getText());

            if (mensagem_erro != null) {
                JOptionPane.showMessageDialog(this, mensagem_erro, "ERRO", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Usuário " + nome_usuarioTextField.getText() + " eliminado com sucesso", "OK", JOptionPane.INFORMATION_MESSAGE);
                limpa();
                eliminarButton.setEnabled(false);
                editar_Button.setEnabled(false);

            }
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

    private void novoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_novoButtonActionPerformed
        setEdição(true);
        editarSalvar();
        limpa();
        setAtivo(true);
        nome_usuarioTextField.grabFocus();
        novoButton.setEnabled(false);
        eliminarButton.setEnabled(false);
        consultarButton.setEnabled(false);
        editar_Button.setEnabled(false);
        cancelaButton.setEnabled(true);
        cod_usuarioTextField.setText(Usuario.id());
    }//GEN-LAST:event_novoButtonActionPerformed

    static public void mostraDados(Usuario usuario) {
        limpa();
        setAtivo(true);
        gravarButton.setEnabled(false);
        cod_usuarioTextField.setText(String.valueOf(usuario.getCódigo()));
        nome_usuarioTextField.setText(usuario.getNome());
        cargoComboBox.setSelectedItem(FichaCadastro.Cargo.valueOf(usuario.getCargo()));
        senhaPasswordField.setText(usuario.getSenha());
        confirma_senhaPasswordField.setText(usuario.getSenha());
        setEdição(false);
        editarSalvar();

    }

    private boolean confereSenhaIguais() {
        if (senhaPasswordField.getText().equals(confirma_senhaPasswordField.getText())) {
            return true;
        } else {
            return false;
        }
    }

