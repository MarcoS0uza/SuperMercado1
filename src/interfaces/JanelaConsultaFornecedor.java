    private void consulta_fornecedorTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_consulta_fornecedorTableMousePressed
        if (evt.getClickCount() == 2) {
            int linha = consulta_fornecedorTable.getSelectedRow();
            int colunaCod = 0;
            String codigo_temp = consulta_fornecedorTable.getValueAt(linha, colunaCod).toString();
            int codigo = Integer.valueOf(codigo_temp).intValue();
            Fornecedor fornecedor = Fornecedor.buscarFornecedor(codigo);
            JanelaCadastroFornecedor.mostraDados(fornecedor);
            dispose();
        }
    }//GEN-LAST:event_consulta_fornecedorTableMousePressed

