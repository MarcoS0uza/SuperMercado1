    static public void mostraDados(Vendedor vendedor) {
        limpa();
        setAtivo(true);
        if (vendedor == null) {
            JOptionPane.showMessageDialog(null, "Vendedor = NULL");
        } else {
            gravarButton.setEnabled(false);
            cod_vendedorTextField.setText(String.valueOf(vendedor.getCodigo()));
            nome_vendedorTextField.setText(vendedor.getNome());
            comissaoTextField1.setText(String.valueOf(vendedor.getComissao()));
            if (vendedor.getUsuario().getCódigo() == 0) {
                cod_usuarioTextField.setText("");
            } else {
                cod_usuarioTextField.setText(String.valueOf(vendedor.getUsuario().getCódigo()));
            }
            nome_usuarioTextField.setText(vendedor.getUsuario().getNome());
            setEdição(false);
            editar_Button.setEnabled(true);
            eliminarButton.setEnabled(true);
            editarSalvar();
        }
    }


