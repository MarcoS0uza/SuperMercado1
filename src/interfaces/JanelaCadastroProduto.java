            imagem_produtoLabel.setIcon(new ImageIcon(j.getSelectedFile().getAbsolutePath()));
            url_imagemTextField.setText(j.getSelectedFile().getAbsolutePath().replace("\\", "\\\\"));
            System.out.println(j.getSelectedFile().getAbsolutePath());
