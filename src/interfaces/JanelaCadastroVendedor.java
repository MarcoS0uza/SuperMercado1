    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        Usuario usuario = new Usuario(Integer.valueOf(cod_usuarioTextField.getText()));

        Vendedor vendedor = new Vendedor(Integer.valueOf(cod_vendedorTextField.getText()).intValue(), nome_vendedorTextField.getText(), Float.valueOf(comissaoTextField1.getText().replace(",", ".")), usuario);

        String mensagem_erro;
        if (!dadosOK(vendedor)) {
            mensagem_erro = "Nome do vendedor é obrigatorio";
        } else {
            mensagem_erro = Vendedor.alterarVendedor(vendedor);
        }
        if (mensagem_erro != null) {
            JOptionPane.showMessageDialog(this, mensagem_erro, "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, nome_vendedorTextField.getText() + " alterado com sucesso");
            setEdição(false);
            editarSalvar();
        }

    }//GEN-LAST:event_salvarButtonActionPerformed

