    public JanelaConsultaFornecedor() {
        initComponents();
        ArrayList<Fornecedor> fornecedores = Fornecedor.visãoTabela();
        if (fornecedores == null) {
            JOptionPane.showMessageDialog(this, "Erro ao recuperar os dados");
        } else {
            ModeloTabela modelo = new ModeloTabela(new String[]{"Código", "Nome", "Cnpj", "Data Cadastro"}, fornecedores.size(), 4);
            for (int i = 0; i < fornecedores.size(); i++) {
                modelo.setValueAt(fornecedores.get(i).getCódigo(), i, 0);
                modelo.setValueAt(fornecedores.get(i).getNome(), i, 1);
                modelo.setValueAt(fornecedores.get(i).getN_documento(), i, 2);
                modelo.setValueAt(fornecedores.get(i).getData_cadastro(), i, 3);

            }
            consulta_fornecedorTable.setModel(modelo);
        }

    }

